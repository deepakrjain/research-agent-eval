"""
judge.py — Independent LLM evaluator for agent answers.

WHY LLM-AS-A-JUDGE:
Manual evaluation of 20-50 questions after every code change is too
slow. Traditional NLP metrics (BLEU, ROUGE) are terrible at semantic
equivalence (e.g. "It's blue" vs "The color is blue").

An LLM judge solves this by semantically comparing the agent's answer
to a verified ground-truth reference answer.

WHY A DIFFERENT MODEL:
We use a different model family for the judge (e.g. Mixtral) than the
agent (e.g. Llama 3) to prevent "self-preference bias." LLMs tend to
artificially inflate the scores of text generated by their own model
family because the stylistic patterns match their internal weights.
"""

import json
from groq import Groq
from agent.config import get_groq_api_key, JUDGE_MODEL
from agent.models import EvaluationScore

JUDGE_SYSTEM_PROMPT = """You are an impartial, expert evaluator. Your job is to score an AI agent's answer against a verified reference answer.

Rules for scoring (1-5):
5 - Perfect: Matches the reference answer completely in factuality and covers all key points.
4 - Good: Factually accurate but misses a minor detail from the reference.
3 - Okay: Gets the main gist right but misses significant details or lacks clarity.
2 - Poor: Contains significant factual errors or misses the core of the question.
1 - Completely Wrong: Totally irrelevant or completely contradicts the reference.

Rules for hallucination:
- Set 'is_hallucinated' to true ONLY IF the agent makes a factual claim that contradicts the reference answer OR states something as a concrete fact that is entirely unsupported by the reference.

You MUST respond with a JSON object containing exactly these fields:
{
    "score": 5, // integer 1-5
    "is_hallucinated": false, // boolean
    "reasoning": "Explanation of why this score was given."
}
"""

def evaluate_answer(
    question: str,
    reference_answer: str,
    agent_answer: str,
    groq_client: Groq | None = None
) -> EvaluationScore:
    """
    Score a single agent answer against the reference answer.
    """
    if groq_client is None:
        groq_client = Groq(api_key=get_groq_api_key())

    # We use a strict prompt format to ensure the judge only looks at the
    # comparison between the two texts, not at formatting like [1] citations.
    user_message = f"""Question: {question}

Reference Answer (Ground Truth):
{reference_answer}

Agent's Answer:
{agent_answer}

Evaluate the Agent's Answer against the Reference Answer. Ignore citation markers like [1]. Return the JSON object."""

    try:
        response = groq_client.chat.completions.create(
            model=JUDGE_MODEL,
            messages=[
                {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
                {"role": "user", "content": user_message},
            ],
            temperature=0.0,  # Zero temperature for deterministic evaluation
            max_tokens=256,
            response_format={"type": "json_object"},
        )

        raw_json = response.choices[0].message.content.strip()
        parsed = json.loads(raw_json)
        return EvaluationScore(**parsed)

    except Exception as e:
        # If the judge fails to parse, return a dummy score indicating failure
        return EvaluationScore(
            score=1,
            is_hallucinated=True,
            reasoning=f"Judge failed to evaluate: {e}"
        )
